/* eslint-disable react/prop-types */
import { ThemeProvider } from '@/contexts/ThemeContext';
import './globals.css';
import { getServerSession } from 'next-auth';
import { authOptions } from '@/app/api/auth/[...nextauth]/route';
import React from 'react';
import { AuthProvider } from '@/contexts/AuthContext';
import prismadb from '@/libs/prismadb';
import { Toaster } from 'react-hot-toast';

export const metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({ children }) {
  const session = await getServerSession(authOptions);

  let user = null;

  if (session?.user?.userId) {
    const rawUser = await prismadb.user.findUnique({
      where: { id: session.user.userId },
      include: {
        questions: true,
        comments: true,
        courses: true,
        carts: {
          include: {
            cartTerms: {
              include: {
                term: {
                  include: {
                    courseTerms: {
                      include: {
                        course: {
                          select: {
                            id: true,
                            title: true,
                            cover: true,
                          },
                        },
                      },
                    },
                  },
                },
              },
            },
          },
        },
      },
    });

    // استخراج دوره‌های یکتا از هر سبد خرید
    user = {
      ...rawUser,
      carts: rawUser.carts.map((cart) => {
        const courses = cart.cartTerms.flatMap((cartTerm) =>
          cartTerm.term.courseTerms.map((courseTerm) => courseTerm.course),
        );

        const uniqueCourses = Array.from(
          new Map(courses.map((course) => [course.id, course])).values(),
        );

        return {
          ...cart,
          uniqueCourses, // اضافه کردن لیست دوره‌های یکتا
        };
      }),
    };
  }

  return (
    <ThemeProvider>
      <AuthProvider initialUser={user}>
        <html lang='fa' dir='rtl'>
          <body
            className={`flex flex-col bg-background-light font-main text-text-light antialiased dark:bg-background-dark dark:text-text-dark`}
          >
            {children}
            <Toaster />
          </body>
        </html>
      </AuthProvider>
    </ThemeProvider>
  );
}
